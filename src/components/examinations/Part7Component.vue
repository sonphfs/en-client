        <h4>An Interview with an Airline Genius</h4>
        <p>At a time in which everyone seems to have the same complaints about traveling such as poor service and long delays, Atlantic Jet is a breath of fresh air. Flying on an Atlantic Jet is actually enjoyable. I recently met with Mark Hamil, founder of Atlantic Jet, to discuss how it all began.</p>
        <p>Hamil says that the idea of starting his own airlines came to him while he was sitting in Chicago's O'Hare Airport waiting for a flight. "There I was, running late for a meeting because my flight was cancelled. My friend who was flying on a private corporate jet called me up and offered me a ride," he recalls. "I thought how great it would be if my company had its own jet, too, but of course we couldn't afford one. I thought, though, what if we could buy just part of one." After doing some research, Hamil found out that his idea was unique.</p>
        <p>The idea of people sharing ownership in a jet was a new concept, but received a lot of positive feedback. Many people and companies would love to have a private plane available, and with Atlantic Jet, it becomes possible. Hamil explains, "They pay for a quarter or a half of a plane, and depending upon the percentage they own, that's how much they get to use it. I mean, very few companies need a plane all the time, so why pay for it?"</p>
        <p>"The most important thing is to always have enough planes available for the customers to use." In fact, an Atlantic member is guaranteed a plane at the airport of his or her choice within 24 hours of notification. Hamil's idea for a new type of airline has proven so popular that he hasn't even had to advertise. "We have about a one-year waiting list right now. Most people hear about us from satisfied customers."</p>
